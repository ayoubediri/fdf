long	ft_atoi(const char *nptr)
{
	long	n;
	long	sign;
	int		i;

	i = 0;
	n = 0;
	sign = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
		n = n * 10 + nptr[i++] - '0';
	return (n * sign);
}
